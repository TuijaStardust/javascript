// Array Methods

let awesomeArray = ["awesome", "cool", "nice", "yippee", "wicked"];

// .forEach()
awesomeArray.forEach(doThisFunction);

function doThisFunction(arrayValue) {
    // code that runs for each arrayValue
}

// .map()
// Create a new array based on another array
// Useful to extract certain type of values from object entity items

awesomeArray.map();

// .filter()
// Useful to filter out array items that match conditions
// If items are objects entities, the complete object entity will be added to the new array 
awesomeArray.filter();